const AVAILABLE_LANGS = ['es', 'en', 'fr', 'de', 'it', 'pt'];
const LOCALE_KEY = 'ldpt_locale';
const getValidLanguage = (lang) => (AVAILABLE_LANGS.includes(lang) ? lang : 'es');
const getLangFromUrl = () => {
  try {
    const params = new URLSearchParams(window.location.search || '');
    const langParam = params.get('lang');
    if (!langParam) return null;
    const lang = langParam.toLowerCase();
    return AVAILABLE_LANGS.includes(lang) ? lang : null;
  } catch (e) {
    return null;
  }
};
const getStoredLanguage = () => {
  try {
    const stored = localStorage.getItem(LOCALE_KEY);
    return stored ? getValidLanguage(stored) : null;
  } catch (e) {
    return null;
  }
};
const resolveInitialLanguage = () => {
  const fromUrl = getLangFromUrl();
  if (fromUrl) return fromUrl;
  const stored = getStoredLanguage();
  if (stored) return stored;
  const docLang = (document.documentElement.lang || '').toLowerCase();
  if (docLang && AVAILABLE_LANGS.includes(docLang)) return docLang;
  return 'es';
};
const initialLanguage = resolveInitialLanguage();
document.documentElement.setAttribute('lang', initialLanguage);

